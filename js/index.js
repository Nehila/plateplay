const category_html = `
    <div class="category {selected}" onclick="change_selected_category(this)">
        <span class="category-title">{icon}</span>
        <span class="category-title {selected}">{name}</span>
    </div>
`

const dish_html = `
    <div data-menu='{data}' onclick="dish_description(this)">
        <div>
            <div class="plate" style="{image}"></div>
            <span class="price">{price} MAD</span>
        </div>
        <span class="plate-title">{name}</span>
    </div>
`

const categories_container = document.getElementById('categories')
const dishes = document.getElementById('dishes')

Object.keys(categories).map((value, index) => {
    categories_container.innerHTML += category_html.replace('{icon}', categories[value].icon).replace('{name}', value).replace('{selected}', index === 0 ? 'category-selected': "").replace('{selected}', index === 0 ? 'category-selected': "")
})  


const change_selected_category = (e) => {
    const current_selection = document.getElementsByClassName("category-selected")
    Array.from(current_selection).forEach((value) => {
        value.classList.remove('category-selected');
    });
    e.classList.add('category-selected')
    e.getElementsByTagName('span')[1].classList.add('category-selected')
    render_dishes(e.getElementsByTagName('span')[1].innerText)
}

const render_dishes = (category) => {
    dishes.innerHTML = ''
    categories[category]['food'].map((value) => {
        dishes.innerHTML += dish_html.replace('{image}', `background: linear-gradient(0deg, rgba(0, 0, 0, 0.60) 0%, rgba(0, 0, 0, 0.00) 54.17%), url(${value.image}) no-repeat, #D9D9D9;`).replace('{name}', value.name).replace('{price}', value.price).replace('{data}', JSON.stringify(value))
    })
}

const render_search_dishes = (array_of_dishes) => {
    dishes.innerHTML = ''
    array_of_dishes.map((value) => {
        dishes.innerHTML += dish_html.replace('{image}', `background: linear-gradient(0deg, rgba(0, 0, 0, 0.60) 0%, rgba(0, 0, 0, 0.00) 54.17%), url(${value.image}) no-repeat, #D9D9D9;`).replace('{name}', value.name).replace('{price}', value.price).replace('{data}', JSON.stringify(value))
    })
}

render_dishes('Burgers')

const searchDish = (dishName) => {
    if (dishName == ""){
        return
    }
    const results = [];
    for (const category in categories) {
      const foods = categories[category].food;
      for (const dish of foods) {
        if (dish.name.toLowerCase().includes(dishName.toLowerCase())) {
          results.push(dish);
        }
      }
    }

     
    
    return results.length > 0 ? render_search_dishes(results) : dishes.innerHTML = '';
}


const dish_description = (e) => {
    data = JSON.parse(e.getAttribute('data-menu'))
    console.log(data)
    data_to_save = JSON.stringify(data)
    localStorage.setItem('data', data_to_save)
    window.location = '/food.html'
}

if (localStorage.getItem('cart') !== null){
    document.getElementById('dot').classList.remove('hide')
    document.getElementById('dot').innerText = JSON.parse(localStorage.getItem('cart')).length
}else{
    document.getElementById('dot').classList.add('hide')
}